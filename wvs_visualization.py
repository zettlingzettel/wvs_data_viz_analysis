"""### Log Transformation for Skewed Columns

Log transformation is a mathematical operation that converts a variable by applying the logarithm function. It is commonly used to address **right-skewed (positively skewed)** distributions, where a large number of data points are concentrated on the left side (lower values) and a few extreme values exist on the right side (higher values).

**Why use Log Transformation?**
1.  **Reduce Skewness**: It compresses the larger values and stretches out the smaller values, making the distribution more symmetrical and closer to a normal distribution.
2.  **Stabilize Variance**: It can help in cases where the variability of data increases with the mean, leading to more stable variance across the data range.
3.  **Linearize Relationships**: In regression analysis, log transformations can linearize relationships between variables that are otherwise curvilinear.

**How to Apply:**
*   **`numpy.log()`**: Use this for data that contains only positive values (`> 0`).
*   **`numpy.log1p()`**: This function calculates `log(1+x)`. It's particularly useful when your data contains zero values, as `log(0)` is undefined. By adding 1 before taking the logarithm, you avoid errors and still transform the data effectively.

**Example:** Let's assume you have a column like `Q28` or `J_INTDATE` which had a high number of outliers and might be skewed. We will apply `np.log1p()` to ensure handling of any zero values that might implicitly be there (or result from capping). First, we'll check the skewness before and after the transformation.
"""

