def get_global_stats(df, label):
    z_score = 1.96

    if 'Q50' in df.columns and 'Q235' in df.columns:
        n = len(df)
        fin_mean = df['Q50'].mean()
        fin_std = df['Q50'].std()
        fin_se = fin_std / np.sqrt(n)

        lead_mean = df['Q235'].mean()
        lead_std = df['Q235'].std()
        lead_se = lead_std / np.sqrt(n)
    else:
        raise ValueError("DataFrame does not contain expected 'Q50' or 'Q235' columns for global stats calculation.")

    return pd.DataFrame([
        {
            'Dataset': label,
            'fin_mean': fin_mean,
            'fin_min': fin_mean - (z_score * fin_se),
            'fin_max': fin_mean + (z_score * fin_se),
            'lead_mean': lead_mean,
            'lead_min': lead_mean - (z_score * lead_se),
            'lead_max': lead_mean + (z_score * lead_se),
            'n_size': n
        }
    ])

