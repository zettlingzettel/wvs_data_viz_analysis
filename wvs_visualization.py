import altair as alt
import pandas as pd
import numpy as np

alt.data_transformers.enable("vegafusion")

def generate_mock_data():
    trust_levels = ['Most people can be trusted', 'Need to be very careful', 'Missing/No Answer']
    gov_conf = ['A great deal', 'Quite a lot', 'Not very much', 'None at all', 'Missing/No Answer']
    datasets = ['Original (Raw)', 'KNN Imputed']

    data = []
    for ds in datasets:
        for trust in trust_levels:
            for conf in gov_conf:
                count = np.random.randint(10, 100)
                if ds == 'KNN Imputed' and (trust == 'Missing/No Answer' or conf == 'Missing/No Answer'):
                    count = 0
                data.append({'Trust_Level': trust, 'Gov_Confidence': conf, 'Dataset': ds, 'count': count})
    return pd.DataFrame(data)

aggregated_confidence_data = generate_mock_data()

trust_level_order = ['Most people can be trusted', 'Need to be very careful']
gov_confidence_order = ['A great deal', 'Quite a lot', 'Not very much', 'None at all']

colors = alt.Scale(
    domain=gov_confidence_order,
    range=["#006d77", "#83c5be", "#e29578", "#ae2012"]
)

def create_clean_viz(data_subset, dataset_label):
    chart = alt.Chart(data_subset).transform_joinaggregate(
        row_total='sum(count)',
        groupby=['Trust_Level']
    ).transform_calculate(
        pct_of_total = "datum.count / datum.row_total"
    ).transform_filter(
        (alt.datum.Gov_Confidence != 'Missing/No Answer') &
        (alt.datum.Trust_Level != 'Missing/No Answer')
    ).mark_bar().encode(
        y=alt.Y('Trust_Level:N',
                sort=trust_level_order,
                title="Social Outlook"),
        x=alt.X('pct_of_total:Q',
                title="Share of Total Population",
                axis=alt.Axis(format='%'),
                scale=alt.Scale(domain=[0, 1])),
        color=alt.Color('Gov_Confidence:O',
                        scale=colors,
                        sort=gov_confidence_order,
                        title="Gov Confidence"),
        tooltip=[
            alt.Tooltip('Trust_Level:N'),
            alt.Tooltip('Gov_Confidence:N'),
            alt.Tooltip('pct_of_total:Q', format='.1%', title='Share')
        ]
    ).properties(
        height=200,
        width=500,
        title=alt.TitleParams(text=f"{dataset_label} Analysis", anchor='start')
    )
    return chart


